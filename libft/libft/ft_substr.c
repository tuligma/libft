#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*substr;
	char	*empt;
	size_t	i;

	i = 0;
	if (start >= ft_strlen(s))
	{
		empt = (char *)malloc(sizeof(char) * 1);
		if (empt == NULL)
			return (NULL);
		empt[i] = '\0';
		return (empt);
	}
	if (len > ft_strlen(s) - start)
		len = ft_strlen(s) - start;
	substr = (char *)malloc(sizeof(char) * (len + 1));
	if (substr == NULL)
		return (NULL);
	while (i < len)
	{
		substr[i] = s[i + start];
		i++;
	}
	substr[i] = '\0';
	return (substr);
}
